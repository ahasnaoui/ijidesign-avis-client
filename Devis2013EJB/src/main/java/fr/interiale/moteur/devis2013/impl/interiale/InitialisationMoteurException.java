package fr.interiale.moteur.devis2013.impl.interiale;

public class InitialisationMoteurException extends Exception {


	private static final long serialVersionUID = -2004933219759951161L;

	public InitialisationMoteurException() {
		super();
		
	}

	public InitialisationMoteurException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public InitialisationMoteurException(String arg0) {
		super(arg0);
		
	}

	public InitialisationMoteurException(Throwable arg0) {
		super(arg0);
		
	}

}
