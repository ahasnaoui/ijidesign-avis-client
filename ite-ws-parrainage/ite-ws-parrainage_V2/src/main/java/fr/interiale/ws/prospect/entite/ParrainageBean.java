package fr.interiale.ws.prospect.entite;

public class ParrainageBean {

	
	private String acteurEdeal;
	
	private String bpuFilleul;
	
	private String canalUsage;
	
	private String codesContrat;

	/**
	 * @return the acteurEdeal
	 */
	public String getActeurEdeal() {
		return acteurEdeal;
	}

	/**
	 * @param acteurEdeal the acteurEdeal to set
	 */
	public void setActeurEdeal(String acteurEdeal) {
		this.acteurEdeal = acteurEdeal;
	}

	/**
	 * @return the bpuFilleul
	 */
	public String getBpuFilleul() {
		return bpuFilleul;
	}

	/**
	 * @param bpuFilleul the bpuFilleul to set
	 */
	public void setBpuFilleul(String bpuFilleul) {
		this.bpuFilleul = bpuFilleul;
	}

	/**
	 * @return the canalUsage
	 */
	public String getCanalUsage() {
		return canalUsage;
	}

	/**
	 * @param canalUsage the canalUsage to set
	 */
	public void setCanalUsage(String canalUsage) {
		this.canalUsage = canalUsage;
	}

	/**
	 * @return the codesContrat
	 */
	public String getCodesContrat() {
		return codesContrat;
	}

	/**
	 * @param codesContrat the codesContrat to set
	 */
	public void setCodesContrat(String codesContrat) {
		this.codesContrat = codesContrat;
	}
	
	
	
}
